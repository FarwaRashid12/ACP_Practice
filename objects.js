// Normal variable
let car = "Fiat"; 
console.log("Car Name:", car); // Output: Car Name: Fiat

// Object with properties
const carDetails = {
    type: "Fiat",
    model: "500",
    color: "white"
};

// Accessing object properties
console.log("Car Type:", carDetails.type);   // Output: Car Type: Fiat
console.log("Car Model:", carDetails.model); // Output: Car Model: 500
console.log("Car Color:", carDetails.color); // Output: Car Color: white
